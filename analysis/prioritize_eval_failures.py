from __future__ import annotations

import json
import re
import sys
from collections import Counter, defaultdict
from pathlib import Path
from typing import Any


ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

DEFAULT_EVAL_REPORT_PATH = ROOT / "analysis" / "eval_report.json"
DEFAULT_OUTPUT_JSON_PATH = ROOT / "analysis" / "eval_priorities.json"
DEFAULT_OUTPUT_MD_PATH = ROOT / "analysis" / "eval_priorities.md"
JSONDict = dict[str, Any]
ROOT_CAUSE_ORDER = (
    "missing_kb_unit",
    "missing_synonym",
    "wrong_category_match",
    "wrong_law_priority",
    "needs_clarification_but_answered",
    "should_answer_but_clarification",
    "unknown",
)
ABBREVIATION_PATTERN = re.compile(r"\b(?:[A-Z]{2,}[A-Z0-9-]{0,6}|[A-Z]{1,4}-\d{1,4}|JDG)\b")


def load_eval_report(path: str | Path = DEFAULT_EVAL_REPORT_PATH) -> JSONDict:
    """Load the JSON report generated by ``analysis/run_eval.py``."""

    file_path = Path(path)
    payload = json.loads(file_path.read_text(encoding="utf-8"))
    if not isinstance(payload, dict) or "records" not in payload:
        raise ValueError(f"Invalid eval report format: {file_path}")
    return payload


def _normalize(value: str) -> str:
    return " ".join((value or "").lower().split())


def infer_root_cause(record: JSONDict) -> str:
    """Infer a likely root cause for one failed eval record."""

    expected_behavior = str(record.get("expected_behavior", ""))
    predicted_behavior = str(record.get("predicted_behavior", ""))
    expected_intent = str(record.get("expected_intent", ""))
    predicted_intent = str(record.get("predicted_intent", ""))
    question = str(record.get("question", ""))
    top_chunks = record.get("top_chunks", [])
    top_laws = [str(chunk.get("law_name", "")) for chunk in top_chunks if isinstance(chunk, dict)]
    law_match = bool(record.get("law_match", False))

    if expected_behavior == "ask_follow_up" and predicted_behavior != "ask_follow_up":
        return "needs_clarification_but_answered"

    if expected_behavior == "answer_directly" and predicted_behavior == "ask_follow_up":
        return "should_answer_but_clarification"

    if predicted_intent != expected_intent and predicted_intent not in {"unknown", "out_of_scope"}:
        return "wrong_category_match"

    if predicted_intent == "unknown" and ABBREVIATION_PATTERN.search(question):
        return "missing_synonym"

    if expected_behavior == "answer_directly" and not top_laws:
        return "missing_kb_unit"

    if expected_behavior == "answer_directly" and top_laws and not law_match:
        if predicted_intent == expected_intent:
            return "wrong_law_priority"
        if ABBREVIATION_PATTERN.search(question):
            return "missing_synonym"
        return "missing_kb_unit"

    return "unknown"


def _suggest_fix(root_cause: str, expected_intent: str, expected_law_or_area: str, example_question: str) -> str:
    """Return a concise, developer-facing suggested fix string."""

    if root_cause == "missing_kb_unit":
        return f"Add or strengthen KB units for {expected_law_or_area} ({expected_intent})."
    if root_cause == "missing_synonym":
        token_match = ABBREVIATION_PATTERN.search(example_question)
        token = token_match.group(0) if token_match else expected_intent
        return f"Add synonym or abbreviation coverage for '{token}' in {expected_intent} questions."
    if root_cause == "wrong_category_match":
        return f"Adjust intent routing so {expected_intent} queries are not misclassified."
    if root_cause == "wrong_law_priority":
        return f"Improve retrieval ranking so {expected_law_or_area} wins over competing laws."
    if root_cause == "needs_clarification_but_answered":
        return f"Tighten clarification rules for underspecified {expected_intent} questions."
    if root_cause == "should_answer_but_clarification":
        return f"Relax clarification gating or add grounding for answerable {expected_law_or_area} questions."
    return f"Review failed {expected_intent} examples manually and refine the weakest layer first."


def build_priority_report(report: JSONDict) -> JSONDict:
    """Aggregate failed eval records into prioritized fix groups."""

    records = report.get("records", [])
    if not isinstance(records, list):
        raise ValueError("Eval report must contain a 'records' list.")

    failed_records = [record for record in records if isinstance(record, dict) and not bool(record.get("behavior_match"))]
    grouped: defaultdict[str, list[JSONDict]] = defaultdict(list)
    issue_groups: defaultdict[tuple[str, str, str], list[JSONDict]] = defaultdict(list)

    for record in failed_records:
        root_cause = infer_root_cause(record)
        grouped[root_cause].append(record)
        key = (
            root_cause,
            str(record.get("expected_intent", "unknown")),
            str(record.get("expected_law_or_area", "")),
        )
        issue_groups[key].append(record)

    groups_summary: list[JSONDict] = []
    for root_cause in sorted(grouped, key=lambda value: (ROOT_CAUSE_ORDER.index(value) if value in ROOT_CAUSE_ORDER else 999, value)):
        items = grouped[root_cause]
        total_frequency = sum(int(item.get("source_frequency", 1) or 1) for item in items)
        groups_summary.append(
            {
                "root_cause": root_cause,
                "count": len(items),
                "total_frequency": total_frequency,
                "example_questions": [str(item.get("question", "")) for item in items[:3]],
            }
        )

    suggested_fixes: list[JSONDict] = []
    for (root_cause, expected_intent, expected_law_or_area), items in issue_groups.items():
        total_frequency = sum(int(item.get("source_frequency", 1) or 1) for item in items)
        example_questions = [str(item.get("question", "")) for item in items[:3]]
        suggested_fixes.append(
            {
                "root_cause": root_cause,
                "expected_intent": expected_intent,
                "expected_law_or_area": expected_law_or_area,
                "count": len(items),
                "total_frequency": total_frequency,
                "example_questions": example_questions,
                "suggested_fix": _suggest_fix(root_cause, expected_intent, expected_law_or_area, example_questions[0] if example_questions else ""),
            }
        )

    suggested_fixes.sort(
        key=lambda item: (
            -int(item["total_frequency"]),
            -int(item["count"]),
            str(item["root_cause"]),
            str(item["expected_law_or_area"]),
        )
    )

    return {
        "overall": report.get("overall", {}),
        "failed_records": len(failed_records),
        "root_cause_summary": groups_summary,
        "suggested_fixes": suggested_fixes,
    }


def render_markdown(priority_report: JSONDict, top_n: int = 20) -> str:
    """Render a human-readable markdown table of the top suggested fixes."""

    lines = [
        "# Eval Failure Priorities",
        "",
        f"Failed records: **{priority_report.get('failed_records', 0)}**",
        "",
        "## Root cause summary",
        "",
        "| Root cause | Count | Total frequency | Example questions |",
        "| --- | ---: | ---: | --- |",
    ]

    for item in priority_report.get("root_cause_summary", []):
        examples = "<br>".join(item.get("example_questions", []))
        lines.append(
            f"| {item['root_cause']} | {item['count']} | {item['total_frequency']} | {examples} |"
        )

    lines.extend(
        [
            "",
            "## Top 20 suggested fixes",
            "",
            "| Rank | Root cause | Count | Total frequency | Expected area | Suggested fix | Example question |",
            "| ---: | --- | ---: | ---: | --- | --- | --- |",
        ]
    )

    for index, item in enumerate(priority_report.get("suggested_fixes", [])[:top_n], start=1):
        example = item.get("example_questions", [""])[0]
        lines.append(
            f"| {index} | {item['root_cause']} | {item['count']} | {item['total_frequency']} | "
            f"{item['expected_law_or_area']} | {item['suggested_fix']} | {example} |"
        )

    lines.append("")
    return "\n".join(lines)


def write_priority_outputs(
    priority_report: JSONDict,
    *,
    json_path: str | Path = DEFAULT_OUTPUT_JSON_PATH,
    markdown_path: str | Path = DEFAULT_OUTPUT_MD_PATH,
) -> tuple[Path, Path]:
    """Write the JSON and Markdown prioritization artifacts to disk."""

    json_file = Path(json_path)
    markdown_file = Path(markdown_path)
    json_file.parent.mkdir(parents=True, exist_ok=True)
    markdown_file.parent.mkdir(parents=True, exist_ok=True)
    json_file.write_text(json.dumps(priority_report, ensure_ascii=False, indent=2, sort_keys=True) + "\n", encoding="utf-8")
    markdown_file.write_text(render_markdown(priority_report), encoding="utf-8")
    return json_file, markdown_file


def main() -> None:
    """CLI entry point for eval failure prioritization."""

    report = load_eval_report()
    priorities = build_priority_report(report)
    write_priority_outputs(priorities)
    print(json.dumps({"failed_records": priorities["failed_records"]}, ensure_ascii=False))


if __name__ == "__main__":
    main()
